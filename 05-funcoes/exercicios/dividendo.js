function dividendAndRestOfTheDivision(number1, number2)
{

    let division = number1 / number2

    let rest = number1 % number2

    console.log(`${number1} / ${number2} = ${division}`)
    console.log(`O resto da divisão de ${number1} por ${number2} é ${rest}`)

}

dividendAndRestOfTheDivision(6, 2)