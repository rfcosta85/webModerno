// Crie uma função que dado 2 valores nos mostre o resultado a soma, subtração, multiplicação e divisão

function maths(number1, number2)
{

   console.log(`${number1} + ${number2} = ${number1 + number2}`)
   console.log(`${number1} - ${number2} = ${number1 - number2}`)
   console.log(`${number1} x ${number2} = ${number1 * number2}`)
   console.log(`${number1} / ${number2} = ${number1 / number2}`)
   
}

maths(2, 4)

